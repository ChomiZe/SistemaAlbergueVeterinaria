    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            if (this.txtCantidadCuotas.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ingrese cantidad de cuotas");
                return;
            }
           
            if (this.txtMontoCuota.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ingrese un monto de cuota");
                return;
            }
            
            if (Integer.parseInt(this.Label2.getText()) > Integer.parseInt(this.lbl1.getText())){
                JOptionPane.showMessageDialog(this, "No puede ser mayor que el monto total de la cuota");
                return;
            }
            
            
            this.CantidadCuotas = this.txtCantidadCuotas.getText();
            this.MontoCuota = this.txtMontoCuota.getText();
            this.aceptar = true;
            this.PeriodoCuota = comJCombox.getComboxString(comboPeriodo, 1);
            this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

