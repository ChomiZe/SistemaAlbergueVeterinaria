    private void mostrarProveed() {
        tabla.setRowCount(0);
        tabla = (DefaultTableModel)tablaProveedores.getModel();
        sentencia = "SELECT * FROM Proveedores";
        try {
        resultado = ejecutar.executeQuery(sentencia);
        while(resultado.next()){
                Object fila[] = new Object[5];
                fila[0] = resultado.getInt(1);
                fila[1] = resultado.getString(2);
                fila[2] = resultado.getInt(3);
                fila[3] = resultado.getInt(4);
                fila[4] = resultado.getString(5);
             
             
                tabla.addRow(fila);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmProveedores.class.getName()).log(Level.SEVERE, null, ex);
    }
  
    }

    private void accionCancelar() {
         modoEdicion(false);
       limpiarCuadros();
       }

    private void accionModificar() {
        if(tablaProveedores.getSelectedRow()>=0){
            operacion = 'M';
            modoEdicion(true);
            txtDescripprov.grabFocus();
       }else{
            JOptionPane.showMessageDialog(this,"No se ha seleccionado ningun registro","Seleccione...",JOptionPane.ERROR_MESSAGE);
        }
        }

    private void accionEliminar() {
             int fsel = tablaProveedores.getSelectedRow();
        if(fsel>=0){
             String xid = tablaProveedores.getValueAt(fsel, 0).toString();
             String xdes = tablaProveedores.getValueAt(fsel,1).toString();
             int resp = JOptionPane.showConfirmDialog(this, "Confirma que desea eliminar la Proveedores *"+xdes.toUpperCase()+"* del Sistema?","Confirme...",
                    JOptionPane.YES_NO_OPTION);
              if(resp == JOptionPane.YES_OPTION){
                sentencia = "DELETE FROM Proveedores WHERE idProveedores='"+xid+"'";
                 try {
                    ejecutar.executeUpdate(sentencia);
                } catch (SQLException ex) {
                    Logger.getLogger(FrmProveedores.class.getName()).log(Level.SEVERE, null, ex);
                } 

    }
              mostrarProveed();
              limpiarCuadros();
        }
        }

